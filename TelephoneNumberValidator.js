function telephoneCheck(str) {
let telephoneRegex = /1?[-\s_]?\(?\d{3}[-_\s)]?\s?\d{3}[-_\s]?\d{4}/g;
  if (str.match(telephoneRegex)) {
    return true;
  }
  else {
    return false;
  }
}

